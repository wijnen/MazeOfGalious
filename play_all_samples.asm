;; Compiling:
;;   z80asm play_all_samples.asm --output play_all_samples.rom
;; 
;; Running:
;;   openmsx play_all_samples.rom
;; 
;; Note: openmsx refuses if the compiled file ends in .bin, like a.bin.

org 0x4000

;; Cartridge header +0: ID. Must be "AB".
defm "AB"

;; Cartridge header +2: INIT. Define the entry point.
defw init

;; Cartridge header +4: STATEMENT.
defw 0x0000

;; Cartridge header +6: DEVICE.
defw 0x0000

;; Cartridge header +8: TEXT.
defw 0x0000

;; Cartridge header +10: Reserved. 6 bytes reserved for future updates.
defw 0x0000
defw 0x0000
defw 0x0000


;; Function : Compares HL with DE
;; Input    : HL, DE
;; Output   : Zero flag set if HL and DE are equal. Carry flag set if HL is less than DE.
;; Registers: AF
DCOMPR: equ	0x0020

;; Function: Switches indicated slot at indicated page.
;; Input:
;;   A - Slot ID, see RDSLT
;;   H - Bit 6 and 7 must contain the page number (00-11)
ENASLT:	equ	0x0024

;; Function: Writes data to PSG register
;; Input:
;; A - PSG register number
;; E - Data to write
WRTPSG:	equ	0x0093

RSLREG:	equ	0x0138

;; Function: Reads VDP status register (clear interrupts)
;; Output:
;;   A - Value which was read
RDVDP:	equ	0x013E

EXPTBL:	equ	0xfcc1
EXPTBL0:equ	0xfcc1	;; Slot 0: 0x80 = expanded, 0 = not expanded. Also main BIOS-ROM slot address.
EXPTBL1:equ	0xfcc2	;; Slot 1: 0x80 = expanded, 0 = not expanded.
EXPTBL2:equ	0xfcc3	;; Slot 2: 0x80 = expanded, 0 = not expanded.
EXPTBL3:equ	0xfcc4	;; Slot 3: 0x80 = expanded, 0 = not expanded.
SLTTBL:	equ	0xfcc5	;;
SLTTBL0:equ	0xfcc5	;; Mirror of slot 0 secondary slot selection register.
SLTTBL1:equ	0xfcc6	;; Mirror of slot 1 secondary slot selection register.
SLTTBL2:equ	0xfcc7	;; Mirror of slot 2 secondary slot selection register.
SLTTBL3:equ	0xfcc8	;; Mirror of slot 3 secondary slot selection register.
H.KEYI:	equ	0xfd9a	;; The H.KEYI hook is invoked every interrupt.
H.TIMI:	equ	0xfd9f	;; The H.TIMI hook is invoked at regular intervals. 50 or 60 times per second.
H.NMI:	equ	0xfdd6	;;

var_start:				equ 0xe000
play_sample__sample_table_addr:		equ 0xe000
play_sample__sample_start_addr:		equ 0xe002
play_sample__sample_opcode_ptr:		equ 0xe004



;; Should start at 0x4010.
interrupt_hook:
  CALL RDVDP		;; Clear interrupt.
  DI			;; Disable interrupts.
  
  ;CALL play_sound02
  CALL play_sample
  
  interrupt_hook_end:
    CALL RDVDP		;; Clear interrupt.
    EI			;; (Re)enable interrupts.
    RET			;; Interrupt done.



;; This address is the starting point of the ROM, as defined in the 
;; cartridge header.
init:
  ;; Disable interrupts during init.
  DI
  
  ;; Set the interrupt mode.
  IM 1
  
  CALL bootstrap
  LD H, 0x80
  ;; Switch to that slot in page 2.
  CALL ENASLT
  
  ;; Set the H.TIMI hook to "JP interrupt_hook".
  LD A, 0xC3		;; 0xC3 is the opcode for JP (jump unconditionally).
  LD (H.TIMI), A
  LD HL,interrupt_hook
  LD (H.TIMI+1), HL
  LD A, 0xC9
  LD (H.TIMI+3), HL	;; End with explicit "RET".
  
  ;; Map pages D+E+F.
  LD A, 0x0D		;;
  LD (0x6000), A	;; Map page D (0x1a000-0x1bfff).
  LD A, 0x0E		;;
  LD (0x8000), A	;; Map page E (0x1c000-0x1dfff).
  LD A, 0x0F		;;
  LD (0xA000), A	;; Map page F (0x1e000-0x1ffff).
  
  ;; Clear memory.
  LD A, 0x00
  LD (var_start), A
  LD BC, 0x0FFF
  LD HL, 0xE000
  LD DE, 0xE001
  LDIR
  
  ;; Clear pending interrupts.
  CALL RDVDP
  
  ;; Enable interrupts.
  EI



;; Main loop.
main:
  ;; "HALT" doesn't seem to work.
  JP main



bootstrap:
  ;; Our ROM can be in any/either cartridge slot. Bootstrap.
  
  ;; RSLREG: read slot register to register A.
  ;; bit0+1: Slot mapped to page 0 (0x0000-0x3FFF)
  ;; bit2+3: Slot mapped to page 1 (0x4000-0x7FFF)
  ;; bit4+5: Slot mapped to page 2 (0x8000-0xBFFF)
  ;; bit6+7: Slot mapped to page 3 (0xC000-0xFFFF)
  CALL RSLREG
  
  ;; Set A to the slot that is mapped in page 1: the cartridge.
  RRCA
  RRCA
  AND 0x3
  
  ;; BC = A
  LD C, A
  LD B, 0

  ;; Is our cartridge slot expanded? 0x80 if it is, 0x00 otherwise.
  LD HL, EXPTBL		;; Start of expansion table.
  ADD HL, BC		;; Go to index of "our" slot.
  LD A, (HL)
  AND 0x80
  
  OR C
  LD C, A		;; bit 7: slot expanded or not, bit 0+2: slot number/index.
  
  ;; Go to the same index, but in table SLTTBL. It contains the state of 
  ;; mapper for slots.
  INC HL
  INC HL
  INC HL
  INC HL
  
  LD A, (HL)		;; Read "our" SLTTBL.
  AND 0x0C		;; State of our cartridge.
  OR C			;; bit 7: is_expanded; bit 2-3: subslot; bit 0-1: slot
  
  RET



HL_becomes_deref_BC:
  LD A, (BC)
  LD L, A
  INC BC
  LD A, (BC)
  LD H, A
  RET
  NOP

seek 0x100
org 0x4100
play_sample:
  LD HL, (play_sample__sample_table_addr)	;; Get address of start of sample.
  LD DE, 0x0000					;; 0x000 means: not initialized, or done with last sample.
  CALL DCOMPR
  JP NZ, play_sample__skip_bootstrap
  
  play_sample__bootstrap:
    LD BC, sample_table_1			;;
    LD (play_sample__sample_table_addr), BC	;;
    CALL HL_becomes_deref_BC			;;
    LD (play_sample__sample_start_addr), HL	;;
    LD (play_sample__sample_opcode_ptr), HL	;;
    JP play_sample__start_parsing		;;
  
  play_sample__skip_bootstrap:
    LD BC, (play_sample__sample_table_addr)	;;
    CALL HL_becomes_deref_BC			;;
    LD DE, (play_sample__sample_opcode_ptr)	;;
    CALL DCOMPR
    JP NZ, play_sample__start_parsing
    
    play_sample__advance_to_next_sample:
      LD BC, (play_sample__sample_table_addr)
      ;; Advance to next sample pointer.
      INC BC
      INC BC
      
      LD (play_sample__sample_table_addr), BC
      CALL HL_becomes_deref_BC
      LD (play_sample__sample_start_addr), HL
      LD (play_sample__sample_opcode_ptr), HL
      
      LD DE, 0x0000
      CALL DCOMPR
      JP NZ, play_sample__start_parsing
      
      play_sample__reached_end_of_sample_table:
        ;; Force a (re)bootstrap.
        LD (play_sample__sample_table_addr), DE
        JP play_sample
  
  play_sample__start_parsing:
  LD HL, (play_sample__sample_opcode_ptr)
  
  LD A, (HL)		;; Read opcode.
  LD B, A		;; Save opcode.
  
  ;; Update pointer to next opcode.
  INC HL
  LD (play_sample__sample_opcode_ptr), HL
  
  ;; Return if end of sound.
  CP 0xFF
  JP Z, play_sample_0xFF
  
  ;; Return if loop.
  CP 0xFE
  JP Z, play_sample_0xFE
  
  ;; Compare high nybble.
  AND 0xF0
  CP 0x20
  JP Z, play_sample_0x2y
  
  CP 0x10
  JP Z, play_sample_0x1y
  
  ;; Nothing special, just volume and frequency divider.
  ;;
  ;; High nybble is the volume, but we need it in the low nybble.
  RLCA
  RLCA
  RLCA
  RLCA
  
  ;; Set volume for channel A.
  LD E, A	;; WRTPSG: value (volume).
  LD A, 0x08	;; WRTPSG: register (volume of channel A).
  CALL WRTPSG	;; Set the volume.
  
  ;; Set the MSB (4 bit) of the frequency divider.
  LD A, B	;; Restore opcode.
  AND A, 0x0F	;; Select low nybble.
  LD E, A	;; WRTPSG: value (MSB freq div).
  LD A, 0x01	;; WRTPSG: register (MSB freq div channel A).
  CALL WRTPSG
  
  ;; Set the LSB (8 bit) of the frequency divider.
  LD A, (HL)
  INC HL
  LD (play_sample__sample_opcode_ptr), HL
  LD E, A	;; WRTPSG: value (LSB freq div).
  LD A, 0x00	;; WRTPSG: register (LSB freq div channel A).
  CALL WRTPSG
  
  RET
  
  
  
  play_sample_0x1y:
    LD A, B	;; Restore opcode.
    AND 0x0F	;; Select low nybble.
    RLCA	;; Multiply by 2.
    LD E, A	;; WRTPSG: value (noise period/frequency)
    LD A, 0x06	;; WRTPSG: regsiter (noise period/frequency)
    JP WRTPSG
    ;JP play_sample_0x1y
  
  play_sample_0x2y:
    ;; Read duration (in VDP ticks).
    LD A, (HL)
    INC HL
    LD (play_sample__sample_opcode_ptr), HL
    
    ;; PSG register 7: bit 7 MUST be 1, bit 6 MUST be 0, mute all channels.
    LD A, 0xBF
    
    play_sample_0x2y_bit0:
      ;; bit 0: noise
      BIT 0, B
      JP Z, play_sample_0x2y_bit1
      AND 0xF7		;; Clear bit 3 (enable noise for channel A).
    
    play_sample_0x2y_bit1:
      ;; bit 1: tone
      BIT 1, B
      JP Z, play_sample_0x2y_change_mixer_settings
      AND 0xFE		;; Clear bit 0 (enable tone for channel A).
    
    play_sample_0x2y_change_mixer_settings:
      LD E, A		;; WRTPSG: value
      LD A, 0x07	;; WRTPSG: register (change mixer settings).
      JP WRTPSG
  
  play_sample_0xFE:
    ;; FIXME
    INC HL
    INC HL
    INC HL
    LD (play_sample__sample_opcode_ptr), HL
    RET
  
  play_sample_0xFF:
    LD HL, (play_sample__sample_start_addr)
    LD (play_sample__sample_opcode_ptr), HL
    RET



play_sound00:
  LD A, 0x00
  LD E, 0xFE
  CALL WRTPSG
  
  LD A, 0x01
  LD E, 0x00
  CALL WRTPSG
  
  LD E, 0xE
  LD A, 0x08
  CALL WRTPSG
  
  RET



play_sound02:
	LD A, (0xE002)
	OR A
	JP NZ,skip_init
		LD DE, mog_startgame_A5
		;LD DE, mog_startgame_E6
		;LD DE, mog_startgame_G6
		;LD DE, mog_startgame_Csharp6
		;LD DE, mog_startgame_E7
		LD (0xE000), DE
		
		LD HL, 0xE002
		LD (HL), 0x01
	skip_init:
	LD HL, (0xE000)
	
	LD A, (HL)	;; Get volume and MSB.
	CP 0xFF
	JP NZ,not_end_of_sound
		LD HL, 0xE002
		LD (HL), 0x00 
		RET Z		;; End of sound.
	
	not_end_of_sound:
	;; Set volume (4 bit).
	AND 0xF0
	RRCA
	RRCA
	RRCA
	RRCA
	LD E, A
	LD A, 0x08
	CALL WRTPSG
	
	;; Set MSB (4 bit) of frequency/note.
	LD A, (HL)
	AND 0x0F
	LD E, A
	LD A, 0x01
	CALL WRTPSG
	
	;; Set LSB (8 bit) of frequency/note.
	INC HL
	LD A, (HL)
	LD E, A
	LD A, 0x00
	CALL WRTPSG
	
	INC HL
	LD (0xE000), HL
	
	RET

